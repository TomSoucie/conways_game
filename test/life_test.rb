Welcome to Conway's Game of Life

Game will be played on a 10x10 grid, with the 4 main rules as listed below

1. Any live cell with fewer than two live neighbours dies, as if caused by underpopulation.
2. Any live cell with two or three live neighbours lives on to the next generation.
3. Any live cell with more than three live neighbours dies, as if by overpopulation.
4. Any dead cell with exactly three live neighbours becomes a live cell, as if by reproduction.

Enter cells you want to start as alive with the grid format nomeclature of "A1" where the letter
is the row and the number is the column where you want the new cell to live